    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Please select a student to update.");
        return;
    }

    String id = jTable1.getValueAt(selectedRow, 0).toString();
    String name = nametxt.getText();
    String email = emailtxt.getText();
    String genderselect = (String) gender.getSelectedItem();
    String address = addresstxt.getText();

    if (name.isEmpty() || email.isEmpty() || address.isEmpty() || genderselect.equals("Select Gender")) {
        JOptionPane.showMessageDialog(null, "Please fill all fields and select a valid gender.");
        return;
    }

    String query = "UPDATE student SET StudentName = ?, Email = ?, Gender = ?, Address = ? WHERE ID = ?";
    try (Connection conn = DBconnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {
        pstmt.setString(1, name);
        pstmt.setString(2, email);
        pstmt.setString(3, genderselect);
        pstmt.setString(4, address);
        pstmt.setInt(5, Integer.parseInt(id));
        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Student updated successfully!");
        loadProducts();
        getAllStatus();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
            return;
        }

        int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            String query = "DELETE FROM student WHERE ID= ?";
            try (Connection conn = DBconnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {
                pstmt.setInt(1, id);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "deleted successfully!");
                loadProducts();
                getAllStatus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_deleteActionPerformed

