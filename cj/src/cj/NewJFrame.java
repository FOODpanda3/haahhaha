    private void loadProducts() {
      String query = "SELECT * FROM student";
    try (Connection conn = DBconnection.getConnection(); 
         Statement stmt = conn.createStatement(); 
         ResultSet rs = stmt.executeQuery(query)) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        while (rs.next()) {
            tableModel.addRow(new Object[]{
                rs.getInt("ID"),
                rs.getString("StudentName"),
                rs.getString("Email"),
                rs.getString("Gender"),
                rs.getString("Address") // FIXED from getDouble to getString
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }

    private void getAllStatus() {
        Set<String> statuses = new HashSet<>();
    gender.removeAllItems();
    gender.addItem("Select Gender");

    String query = "SELECT DISTINCT Gender FROM student"; 
    try (Connection conn = DBconnection.getConnection(); 
         Statement stmt = conn.createStatement(); 
         ResultSet rs = stmt.executeQuery(query)) {
        while (rs.next()) {
            String s = rs.getString("Gender"); 
            if (statuses.add(s)) {
                gender.addItem(s);
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error fetching genders: " + e.getMessage());
    }
    }

    private void addTableSelectionListener() {
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow >= 0) {
                nametxt.setText(jTable1.getValueAt(selectedRow, 1).toString());
                emailtxt.setText(jTable1.getValueAt(selectedRow, 2).toString());
                gender.setSelectedItem(jTable1.getValueAt(selectedRow, 3).toString());
                addresstxt.setText(jTable1.getValueAt(selectedRow, 4).toString());
            }
        });
    }

    private void performSearch() {
      String keyword = searchtxt.getText().trim();
    System.out.println("Searching for keyword: " + keyword);
    
    String query = "SELECT * FROM student WHERE StudentName LIKE ? OR Email LIKE ? OR Gender LIKE ? OR Address LIKE ?";
    
    try (Connection conn = DBconnection.getConnection(); PreparedStatement ps = conn.prepareStatement(query)) {
        String likeKeyword = "%" + keyword + "%";
        ps.setString(1, likeKeyword);
        ps.setString(2, likeKeyword);
        ps.setString(3, likeKeyword);
        ps.setString(4, likeKeyword);
        
        ResultSet rs = ps.executeQuery();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows
        
        int rowCount = 0;
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("ID"),
                rs.getString("StudentName"),
                rs.getString("Email"),
                rs.getString("Gender"),
                rs.getString("Address")
            });
            rowCount++;
        }
        System.out.println("Rows found: " + rowCount);
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Search failed: " + e.getMessage());
    }
    }
    

